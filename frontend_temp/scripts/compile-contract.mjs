// Compiles contracts/HKDAP.sol with solc and writes a TS artifact (ABI + bytecode)
// to lib/chain/hkdapArtifact.ts. Run: node scripts/compile-contract.mjs
import solc from "solc";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

const source = readFileSync(resolve(root, "contracts/HKDAP.sol"), "utf8");

const input = {
  language: "Solidity",
  sources: { "HKDAP.sol": { content: source } },
  settings: {
    optimizer: { enabled: true, runs: 200 },
    outputSelection: { "*": { "*": ["abi", "evm.bytecode.object"] } },
  },
};

console.log("solc version:", solc.version());
const output = JSON.parse(solc.compile(JSON.stringify(input)));

const errors = (output.errors ?? []).filter((e) => e.severity === "error");
if (errors.length) {
  for (const e of errors) console.error(e.formattedMessage);
  process.exit(1);
}

const contract = output.contracts["HKDAP.sol"].HKDAP;
const abi = contract.abi;
const bytecode = "0x" + contract.evm.bytecode.object;

const ts = `// AUTO-GENERATED by scripts/compile-contract.mjs — do not edit by hand.
// Source: contracts/HKDAP.sol
export const HKDAP_ABI = ${JSON.stringify(abi, null, 2)} as const;

export const HKDAP_BYTECODE =
  "${bytecode}";
`;

const outPath = resolve(root, "lib/chain/hkdapArtifact.ts");
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, ts);
console.log("Wrote", outPath, "bytecode bytes:", (bytecode.length - 2) / 2);
